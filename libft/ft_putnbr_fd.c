#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	int		i;
	char	c;

	if (fd)
	{	
		if (n < 0)
		{
			write(fd, "-", 1);
			if (n == -2147483648)
			{
				write (fd, "2", 1);
				n = n % 1000000000;
			}
			n = n * -1;
		}
		i = n % 10;
		n = n / 10;
		c = i + '0';
		if (n != 0)
			ft_putnbr_fd(n, fd);
		write(fd, &c, 1);
	}
}
